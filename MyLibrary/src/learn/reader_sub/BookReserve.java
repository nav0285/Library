    private void addBook(){
        try{
            statement = connect.createStatement();
            preparedStatement = connect.prepareStatement("select book.book_id from "
                    + "book, book_info where book.isbn=book_info.isbn and "
                    + "book_info.title='"+title.getText()+"' and book.branch_id="
                    + "(select branch_id from branch where name="
                    + "'"+branch_name.getSelectedItem().toString()+"') "
                    + "and book.available=1;");
            resultSet = preparedStatement.executeQuery();
            while(resultSet.next()){
                int bid = resultSet.getInt("book_id");
                book_id.addItem(bid);
            }
        }catch(Exception e){
            
        }
    }

