    private void reserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveActionPerformed
       java.util.Date date=new java.util.Date();
       java.sql.Date sqlDate= new java.sql.Date(date.getTime());
       Integer bid = new Integer(book_id.getSelectedItem().toString());
        try{
           preparedStatement = connect.prepareStatement("insert into reserve(reader_id, book_id,"
                   + "date) values(?, ?, ?);");
           preparedStatement.setInt(1, reader_id);
           preparedStatement.setInt(2, bid);
           preparedStatement.setDate(3, sqlDate);
           preparedStatement.executeUpdate();
           preparedStatement = connect.prepareStatement("update book set available=0 where "
                   + "book_id="+book_id.getSelectedItem().toString()+";");
           preparedStatement.executeUpdate();
           op.setMessage(book_id.getSelectedItem()+" has been reserved for you. And "
                   + "its reserved for you, for the next 2 days.");
            op.setMessageType(1);
            dia = op.createDialog("Success");
            dia.setVisible(true);
       }catch(Exception e){
           
       }
    }//GEN-LAST:event_reserveActionPerformed

