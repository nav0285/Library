    public void information(String name){
        try{
          Class.forName("com.mysql.jdbc.Driver");          
          connect = DriverManager
          .getConnection("jdbc:mysql://localhost/library?"
              + "user=name&password=password");
              
      // statements allow to issue SQL queries to the database
      statement = connect.createStatement();
      preparedStatement = connect.prepareStatement("select branch_id from branch where"
              + " name='"+name+"';");
      resultSet = preparedStatement.executeQuery();
      while(resultSet.next()){
          int bid = resultSet.getInt("branch_id");
          preparedStatement = connect.prepareStatement("select reader_id, count(reader_id) as number_of_books"
                  + " from borrow where branch_id="+bid+" group by reader_id order by count(reader_id)"
                  + " desc limit 10;");
          resultSet = preparedStatement.executeQuery();
          borrowerTable.setModel(DbUtils.resultSetToTableModel(resultSet));
      }
      }catch(Exception e){
            
        }
    }
